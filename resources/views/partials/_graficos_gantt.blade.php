                                    <b>Gráficos de Gantt?</b>
                                </h1>
                            </div>

                            <p>
                                <b>Definição:</b> O diagrama de Gantt é um gráfico usado para ilustrar o avanço das diferentes etapas de um projeto. Os intervalos de tempo representando o início e fim de cada fase aparecem como barras coloridas sobre o eixo horizontal do gráfico. <br><br>

                                Mas, você sabia que é possível exportar o Gráfico de Gantt do seu projeto feito com a ferramenta PERTool?<br><br>

                                É bastante simples esse processo, confere aí!<br><br>

                                <b>1º passo:</b> Abra o projeto na ferramenta PERTool.

                                <img src="img/gantt/01.png" alt="dashboard" class="img-responsive" style="margin-top:50px; margin-left: auto;margin-right: auto;"> <br><br>

                                <b>2º passo:</b> Clique no menu Arquivo > Exportar > Para Gantt PNG.

                                <img src="img/gantt/02.png" alt="dashboard" class="img-responsive" style="margin-top:50px; margin-left: auto;margin-right: auto;"> <br><br>

                                <b>3º passo:</b> Escolha um diretório em seu computador para salvar a imagem do gráfico de Gantt.

                                <img src="img/gantt/03.png" alt="dashboard" class="img-responsive" style="margin-top:50px; margin-left: auto;margin-right: auto;"> <br><br>

                                Pronto, não foi fácil? Se quiser você também pode exportar a imagem PNG da sua rede PERT/CPM para utilizar como bem entender. Para isso, basta ir em Arquivo > Exportar > Para PNG. <br><br>

                                Então é isso pessoal. <br><br>

                                Até a próxima! <br><br>

